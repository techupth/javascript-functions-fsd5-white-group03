// Start coding here
// add
function add(a, b) {
  return a + b;
}
let resultAdd = add(10, 5);
console.log(resultAdd);

// subtract
function subtract(a, b) {
  return a - b;
}
let resultSubtract = subtract(10, 5);
console.log(resultSubtract); // Output: 5

// multiply
function multiply(a, b) {
  return a * b;
}
let resultMultiply = multiply(10, 5);
console.log(resultMultiply); // Output: 50

// divide
function divide(a, b) {
  return a / b;
}
let resultDivide = divide(10, 5);
console.log(resultDivide); // Output: 2
